
function  filterAjax(srch,url,successFn,errorFn)
{
		$.ajax({
      	  
	           type: "GET",
	           url: url,
	           dataType: "JSON",
	           contentType: "application/json; charset=UTF-8",
	           success: successFn,
               error: errorFn
               
	});
}

